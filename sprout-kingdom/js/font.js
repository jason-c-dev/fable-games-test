const _fontCache = new Map(); // color -> {char: canvas}

function _glyphCanvas(ch, color) {
  let byColor = _fontCache.get(color);
  if (!byColor) { byColor = {}; _fontCache.set(color, byColor); }
  if (byColor[ch]) return byColor[ch];
  const g = FONT_GLYPHS[ch];
  if (!g) return null;
  const c = document.createElement('canvas');
  c.width = FONT_W; c.height = FONT_H;
  const x = c.getContext('2d');
  x.fillStyle = color;
  for (let r = 0; r < FONT_H; r++)
    for (let col = 0; col < FONT_W; col++)
      if (g[r][col] === '#') x.fillRect(col, r, 1, 1);
  byColor[ch] = c;
  return c;
}

// Draw text. Uppercases automatically ('x' stays lowercase = multiplication sign).
function drawText(ctx, text, x, y, color = '#fff', scale = 1, shadow = null) {
  const step = (FONT_W + 1) * scale;
  if (shadow) drawText(ctx, text, x + scale, y + scale, shadow, scale, null);
  let cx = Math.round(x);
  for (const raw of String(text)) {
    const ch = raw === 'x' ? 'x' : raw.toUpperCase();
    if (ch !== ' ') {
      const g = _glyphCanvas(ch, color);
      if (g) ctx.drawImage(g, cx, Math.round(y), FONT_W * scale, FONT_H * scale);
    }
    cx += step;
  }
}

function textWidth(text, scale = 1) {
  return String(text).length * (FONT_W + 1) * scale - scale;
}

function drawTextC(ctx, text, cx, y, color = '#fff', scale = 1, shadow = null) {
  drawText(ctx, text, cx - textWidth(text, scale) / 2, y, color, scale, shadow);
}

function drawTextR(ctx, text, rx, y, color = '#fff', scale = 1, shadow = null) {
  drawText(ctx, text, rx - textWidth(text, scale), y, color, scale, shadow);
}
