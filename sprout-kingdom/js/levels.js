// Level data + DSL parser. Levels are hand-authored ASCII grids.
// '|' is a cosmetic column separator (every 16 cols) stripped by the parser.
// Legend: # ground  X stone  = one-way  B brick  ? coin block  M power block
//         G glider block  E moss block  U 1up block  ^ spikes  ~ crumble
//         L lantern  n burrow door  ! goal  2 secret goal
//         S start  c checkpoint  - coin  * dew star
//         b bumble  s snapcap  k spikelet  p puffhawk  l lobber  w wisp  o pod
//
// Authoring rules (validated by tools/verify-levels.js):
//   bumpable blocks sit 3 rows above the standing surface
//   platform chains climb at most 3 rows per hop (4 with a run-up)
//   mandatory gaps are at most 5-6 tiles

const ENTITY_CHARS = {
  b: 'bumble', s: 'snapcap', k: 'spikelet', p: 'puffhawk',
  l: 'lobber', w: 'wisp', o: 'pod',
};

function parseRoom(rows, movers) {
  const clean = rows.map(r => r.replace(/\|/g, ''));
  const w = Math.max(...clean.map(r => r.length));
  const h = clean.length;
  const grid = [];
  const spawns = [];
  let start = null;
  const goals = [], checkpoints = [], doors = [];
  for (let y = 0; y < h; y++) {
    const line = clean[y].padEnd(w, '.');
    const row = new Uint8Array(w);
    for (let x = 0; x < w; x++) {
      const ch = line[x];
      if (ch === '.' || ch === ' ') continue;
      if (CHAR_TILES[ch] !== undefined) {
        row[x] = CHAR_TILES[ch];
        if (ch === '!') goals.push({ x, y, secret: false });
        if (ch === '2') goals.push({ x, y, secret: true });
        if (ch === 'n') doors.push({ x, y });
      } else if (ENTITY_CHARS[ch]) {
        spawns.push({ type: ENTITY_CHARS[ch], x: x * TILE + 8, y: y * TILE + TILE });
      } else if (ch === 'S') {
        start = { x: x * TILE + 8, y: y * TILE + TILE };
      } else if (ch === 'c') {
        checkpoints.push({ x: x * TILE + 8, y: y * TILE + TILE });
      } else if (ch === '-') {
        spawns.push({ type: 'coin', x: x * TILE + 8, y: y * TILE + TILE });
      } else if (ch === '*') {
        spawns.push({ type: 'star', x: x * TILE + 8, y: y * TILE + TILE });
      }
    }
    grid.push(row);
  }
  return {
    w, h, grid, spawns, start, goals, checkpoints, doors,
    movers: (movers || []).map(m => ({
      x: m.tx * TILE, y: m.ty * TILE, x2: m.tx2 * TILE, y2: m.ty2 * TILE,
      w: m.w * TILE, period: m.period,
    })),
  };
}

const _parsedCache = {};
function getLevel(id) {
  if (_parsedCache[id]) return _parsedCache[id];
  const def = LEVELS[id];
  if (!def) throw new Error('unknown level ' + id);
  const rooms = { main: parseRoom(def.rows, def.movers) };
  if (def.bonus) rooms.bonus = parseRoom(def.bonus.rows, def.bonus.movers);
  let starIdx = 0;
  for (const key of ['main', 'bonus']) {
    if (!rooms[key]) continue;
    for (const s of rooms[key].spawns) if (s.type === 'star') s.starIndex = starIdx++;
  }
  const parsed = { ...def, rooms };
  _parsedCache[id] = parsed;
  return parsed;
}

